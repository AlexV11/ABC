int obtener_columna(char c) {

    // (	)	{	}	-	+	*	/	>	=	<	!	.	;	V	A	R	E	D	O	F	L	S	M	I	N	T	H	Z	C	P	"	\n	\t	\r	[a-z]	[A-Z]	[0-9]

    char alfabeto[] = { '(', ')', '{', '}', '-', '+', '*', '/', '>', '=', '<', '!', '.', ';', 'V', 'A', 'R', 'E', 'D', 'O', 'F', 'L', 'S', 'M', 'I', 'N', 'T', 'H', 'Z', 'C', 'P', '"' };
    int i;
    for(i = 0; i < 35; i++){
        if(c == alfabeto[i])
            return i;
    }
    if(islower(c)){
        return i;
    }else if(isupper(c)){
        return i + 1;
    }else if(isdigit(c)){
        return i + 2;
    }else{
        return i + 3;
    }
}

